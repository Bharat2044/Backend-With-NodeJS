let fruit = 'Mango';

switch (fruit) {
    case 'Apple':
        console.log('Apple');
        break;

    case 'Mango':
        console.log('Mango');
        break;

    case 'Banana':
        console.log('Banana');
        break;

    case 'Orange':
        console.log('Orange');
        break;

    default:
        console.log('Not a valid fruit');
        break;
}